    private fun insertIntoList(list: MutableList<HistoryEntry>,
                               songDao: SongInfoDao,
                               artistDao: ArtistInfoDao,
                               title: String,
                               artist: String) {
        val previous = list.lastOrNull()

        val artistInfo = ArtistInfo(name = artist, info = "")
        artistDao.insert(artistInfo)

        val songInfo = SongInfo(title = title, artist = artist)
        val songInfoId = songDao.insert(songInfo)

        val newItem = HistoryEntry(
            timestamp = getBackdatedTimestamp(previous?.timestamp),
            songId = songInfoId,
            artist = artist)
        list.add(newItem)
    }

    private fun getBackdatedTimestamp(previous: DateTime?): DateTime {
        return if (previous != null) {
            previous.minus(3000000)
        } else {
            DateTime.now()
        }
    }

